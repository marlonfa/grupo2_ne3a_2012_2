    private void jBPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBPesquisarActionPerformed
        // TODO add your handling code here:
        if (jRBLogin.isSelected()) {
            this.lista = this.usuarioController.getUsuarioDao().findFilter("login", jTFValorPesquisa.getText());
            this.tableModel = new UsuarioTableModel(lista);
        } else {
            this.lista = this.usuarioController.getUsuarioDao().findFilter("nome", jTFValorPesquisa.getText());
            this.tableModel = new UsuarioTableModel(lista);
        }
        this.jTablePesquisa.setModel(tableModel);
    }//GEN-LAST:event_jBPesquisarActionPerformed

    private void jBEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBEditarActionPerformed
        // TODO add your handling code here:
        UsuarioEditView  view = new UsuarioEditView(new JFrame(), true);
        view.setLocationRelativeTo(null);
        
        int linha = this.jTablePesquisa.getSelectedRow();
        int coluna = Integer.parseInt(jTablePesquisa.getValueAt(linha, 0).toString());
        
        for (int i = 0; i < lista.size(); i++) {
            this.usuarioController.setUsuario((UsuarioEntity) lista.get(i));
            if(this.usuarioController.getUsuario().getId() == coluna)
                break;
        }
        
        view.setUsuarioController(usuarioController);
        view.setVisible(true);
    }//GEN-LAST:event_jBEditarActionPerformed

