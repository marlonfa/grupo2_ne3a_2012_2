    private void jBEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBEntrarActionPerformed
        // TODO add your handling code here:
//        List lista = this.usuarioController.getUsuarioDao().findFilter("login", "admin");
        lista = this.usuarioController.getUsuarioDao().findAll();

        if (lista.size() == 0) {
            createAdmin();
        } else {
            for (int i = 0; i < lista.size(); i++) {
                usuarioController.setUsuario((UsuarioEntity) lista.get(i));
                if (usuarioController.getUsuario().getLogin().equals(jTFLogin.getText())) {
                    if (usuarioController.getUsuario().getSenha().equals(jPFSenha.getText())) {
//                        Imobiliaria1.jLUsuarioLogado.setText("Usu치rio Logado: " + usuarioController.getUsuario().getNome());
                        Imobiliaria.jLUsuarioLogado.setText("Usu치rio Logado: " + usuarioController.getUsuario().getNome());
                        login = true;
                        break;
                    }
                }
            }
        }
        if (login == false) {
            JOptionPane.showMessageDialog(null, "Login ou Senha Inv치lido");
        } else {
            dispose();
        }
    }//GEN-LAST:event_jBEntrarActionPerformed
